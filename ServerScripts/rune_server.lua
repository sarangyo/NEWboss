local function filter(statID, value)
	local text1, text2 = '', (G_stat_text[statID]):gsub('/', '')
	text1 = '아이템 '..text2..' +'..value
	
	return text1
end

local function Rune_Default(var)
	local StrVar = unit.GetStringVar
	if StrVar(var) == nil or StrVar(var) == '' then
		unit.SetStringVar(var, '{"unlock":0}')
	end
end

local function rune_main()
	local j=0
	local data_table = {}
	
	for i, v in ipairs(unit.player.GetItems()) do
		local key = v.dataID
		if rune_data[key] and Server.GetItem(key).type==9 then
			j=j+1
			data_table[j] = {}
			data_table[j].dataID = key
			data_table[j].id = v.id
			data_table[j].Lv = v.level
			data_table[j].OP = Utility.GetItemOptions(v)
			data_table[j].op = {}
			for i, v in ipairs(data_table[j].OP) do
				data_table[j].op[i] = {}
				data_table[j].op[i].statID = v.statID
				data_table[j].op[i].value = v.value
			end
		end
	end
	
	Rune_Default(rune_StringVar)
	
	unit.FireEvent('rune_open', Utility.JSONSerialize(data_table), rune_data.num, rune_data.max_num, unit.GetStringVar(rune_StringVar), Utility.JSONSerialize(stat_table))
end

