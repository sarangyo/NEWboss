Server.GetTopic("룬장착하기").Add(function(T) --교체이기도 함.
	local T1 = Utility.JSONParse(T)
	local key = T1[1]
	local id = T1[2]
	local Tem = unit.player.GetItem(id)
	if not Tem then 
		unit.FireEvent('룬비정상종료')
		return 
	end
	
	local T2 = Utility.JSONParse(unit.GetStringVar(rune_StringVar))
	local T3_id, real_id, lv = 0, 0, 0
	local index = 'id_'..key
	local dumy_table = {}
	if T2[index] then --적용되고있는 룬
		dumy_table = T2[index].op
		local item = Server.CreateItem(T2[index].dataID, 1)
		T3_id = T2[index].dataID
		for i, v in ipairs(T2[index].op) do
			Utility.AddItemOption(item, 3, v.statID, v.value)
		end
		item.level = T2[index].Lv
		lv = item.level
		
		unit.AddItemByTItem(item, false)
		real_id = item.id
	end
	T2[index] = {}
	T2[index].Lv = Tem.level
	T2[index].dataID = Tem.dataID
	T2[index].op = {}
	
	local op_text2 = ''
	for i, v in ipairs(Utility.GetItemOptions(Tem)) do
		op_text2 = op_text2..filter(v.statID, v.value)..'\n'
		T2[index].op[i] = {}
		T2[index].op[i].statID = v.statID
		T2[index].op[i].value = v.value
	end
	T2[index].op_text = op_text2
	
	local txt = Utility.JSONSerialize(T2)
	unit.SetStringVar(rune_StringVar, txt)
	unit.SendCenterLabel('슬롯에 '..Server.GetItem(Tem.dataID).name..'을 끼워넣었습니다.')
	unit.FireEvent('룬장착완료', txt, key, T1[3], T3_id, real_id, Utility.JSONSerialize(dumy_table), lv)
	
	unit.RemoveItemByID(id, 1, false)
	
	local rune_stat = {}
	for _, v in ipairs(stat_table) do
		rune_stat[v] = 0
	end
	
	for i=1, rune_data.max_num do
		local num = T2['id_'..i]
		if num then
			for j, v in ipairs(num.op) do
				rune_stat[v.statID] = rune_stat[v.statID]+v.value
			end
		end
	end
	
	for _, v in ipairs(stat_table) do
		unit.SetVar(rune_NumberVar[v], rune_stat[v])
	end
	
	unit.RefreshStats()
	unit.SendUpdated()
end)

