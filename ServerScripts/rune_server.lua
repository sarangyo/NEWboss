local function OnAddItem(unit, item)
	local id = item.dataID
	if rune_data[id] and item.options[1]==nil then
		local rune = rune_data[id]
		if rune.bool then
			for i, v in ipairs(rune.stat) do
				if rune.rand[i] >= rand(1, 100) then
					local value, range = rune.value[i], rune.range[i]
					Utility.AddItemOption(item, 3, v, rand(value*(1-range), value*(1+range)))
				end
			end
		else
			for i, v in ipairs(rune.stat) do
				local value, range = rune.value[i], rune.range[i]
				Utility.AddItemOption(item, 3, v, rand(value*(1-range), value*(1+range)))
			end
		end
		
		unit.player.SendItemUpdated(item)
	end
end

